class Galaxy{
  static String name;
  static String age;
  static int magnitude;
  static String velocity;
   
  static void setName(String nm){
      name=nm;
  }
  
  static String getName(){
     return name;
  }
  static void setAge(String ag){
    age=ag;
  
  }
  static String getAge(){
  return age;
  }
  static void setMagnitude(int mg){
	  magnitude=mg;
  }
  static int getMagnitude(){
	  return magnitude;
  }
  static void setVelocity(String vl){
  velocity=vl;
  }
  static String getVelocity(){
	  return velocity;
  }
 


  }
