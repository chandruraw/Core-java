class RailwayStation{
  static String name;
  static String train;
  static String destination;
  static int price;
   
  static void setName(String nm){
      name=nm;
  }
  
  static String getName(){
     return name;
  }
  static void setTrain(String trn){
    train=trn;
  
  }
  static String getTrain(){
  return train;
  }
  static void setDestination(String dn){
	  destination=dn;
  }
  static String getDestination(){
	  return destination;
  }
  static void setPrice(int pr){
   price=pr;
  }
  static int getPrice(){
	  return price;
  }
 


  }
