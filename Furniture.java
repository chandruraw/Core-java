class Furniture{
  static String name;
  static int price;
  static String color;
  static String material;
   
  static void setName(String nm){
      name=nm;
  }
  static String getName(){
     return name;
  }
  static void setPrice(int pr){
    price=pr;
  
  }
  static int getPrice(){
  return price;
  }
  static void setColor(String clr){
	  color=clr;
  }
  static String getColor(){
	  return color;
  }
  static void setMaterial(String mtl){
   material=mtl;
  }
  static String getMaterial(){
	  return material;
  }
 

}  